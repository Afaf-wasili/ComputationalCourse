/*Q4: Write a Python program to print the numbers from 1 to 10 using a for and while loop.
 */

#include <iostream>
using namespace std;

int main() {
    // For loop
    for (int i = 1; i <= 10; i++) {
        cout << i << endl;
    }

    // While loop
    int num = 1;
    while (num <= 10) {
        cout << num << endl;
        num++;
    }

    return 0;
}

