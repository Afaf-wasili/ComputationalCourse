'''
how to sum these two arraies without numpy library via using for loop 
a = np.array([10,20,30])
b = np.array([1,2,0])
c = a + b
print(c)
'''


# Define the arrays
a = [10, 20, 30]
b = [1, 2, 0]

# Initialize an empty list to store the result


# Iterate through the elements of the arrays
for i in range(len(a)):
    c[i] = (a[i] + b[i])

# Print the result
print(c)
